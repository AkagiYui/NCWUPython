# 统计每个人旅游过的城市的数目
# 张三去过2个城市
# 统计去过上海的人数以及名单
# 去过上海的有2人，他们是李四、王五

d = {
    "张三": ["北京", "成都"],
    "李四": ["上海", "广州", "兰州"],
    "王五": ["太原", "西安", "济南", "上海"]
}

for k, v in d.items():
    print(f'{k}去过{len(v)}个城市')


count = 0
p = []
for k, v in d.items():
    if '上海' in v:
        count += 1
        p.append(k)
print(f'去过上海的有{count}人，他们是{"、".join(p)}')
